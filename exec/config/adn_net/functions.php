function get_campaign_category_id($name)
{
    $name = strtolower(trim($name));
    $categories = campaign_categories();
    $category = array_search($name, $categories);
    return intval($category);
}

function campaign_categories()
{
    return array(
        CAMPAIGN_CATEGORY_GAME => 'game',
        CAMPAIGN_CATEGORY_APPLICATION => 'application',
        CAMPAIGN_CATEGORY_BRAND => 'brand',
        CAMPAIGN_CATEGORY_E_COMMERCE => 'e-commerce'
    );
}

